public class Link {

    private String endOne;
    private String endTwo;
    
    public Link(String a, String b) {
        endOne = a;
        endTwo = b;
    }
    
    //--------------------------------------------------------------------------
    public String getEndOne() {
        return endOne;
    }
    
    //--------------------------------------------------------------------------
    public String getEndTwo() {
        return endTwo;
    }
}
